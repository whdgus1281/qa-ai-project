# ml_basic.py
# scikit-learn = 파이썬 머신러닝 라이브러리
# 가장 많이 쓰는 머신러닝 도구예요
from sklearn.tree import DecisionTreeClassifier

# =============================================
# ① 학습 데이터 준비
# =============================================

# X = 입력 데이터 (특징)
# 버그 응답시간(초)을 리스트로 만들어요
# [[값]] 처럼 2차원으로 만들어야 해요 (규칙이에요!)
X = [
    [0.5],   # 0.5초
    [0.8],   # 0.8초
    [1.2],   # 1.2초
    [2.1],   # 2.1초
    [3.2],   # 3.2초
    [4.5],   # 4.5초
    [5.0],   # 5.0초
    [0.3],   # 0.3초
]

# y = 정답 레이블
# 0 = Minor, 1 = Critical
# X와 순서가 같아야 해요!
y = [0, 0, 0, 1, 1, 1, 1, 0]
#    ↑  ↑  ↑  ↑  ↑  ↑  ↑  ↑
# 0.5 0.8 1.2 2.1 3.2 4.5 5.0 0.3

# =============================================
# ② AI 모델 만들고 학습시키기
# =============================================

# DecisionTreeClassifier = 결정 트리 분류 모델
# 쉽게 말하면 "스무고개"처럼 질문해서 분류하는 AI예요
model = DecisionTreeClassifier()

# .fit() = 학습! "이 데이터로 공부해!"
# X(입력)와 y(정답)를 같이 줘요
model.fit(X, y)

print("✅ AI 학습 완료!")

# =============================================
# ③ 예측해보기
# =============================================

# .predict() = 예측! "이건 뭐야?"
# [[값]] 형태로 넣어야 해요
결과 = model.predict([[1.0]])  # 1.0초짜리 버그는?
print(f"1.0초 버그 → {'Critical' if 결과[0] == 1 else 'Minor'}")

결과 = model.predict([[3.5]])  # 3.5초짜리 버그는?
print(f"3.5초 버그 → {'Critical' if 결과[0] == 1 else 'Minor'}")

결과 = model.predict([[0.2]])  # 0.2초짜리 버그는?
print(f"0.2초 버그 → {'Critical' if 결과[0] == 1 else 'Minor'}")